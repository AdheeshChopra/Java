public class ArithematicExpressions {
    public static void main(String[] args){
        int result = 10+3;
        System.out.println(result);
        result = 10-3;
        System.out.println(result);
        result = 10*3;
        System.out.println(result);
        int x=1;
        x+=2;
        System.out.println(x);
    }
}
